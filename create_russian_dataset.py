    print("=" * 60)
    print("Создание тестового русского датасета")
    print("=" * 60)

    # Расширяем отзывы
    print("\n[1/3] Генерация отзывов...")
    positive_expanded = expand_reviews(positive_reviews, 'positive', 5000)
    negative_expanded = expand_reviews(negative_reviews, 'negative', 5000)

    # Создаем DataFrame
    print("[2/3] Создание DataFrame...")
    df = pd.DataFrame({
        'review': positive_expanded + negative_expanded,
        'sentiment': ['positive'] * len(positive_expanded) + ['negative'] * len(negative_expanded)
    })

    # Перемешиваем
    df = df.sample(frac=1, random_state=42).reset_index(drop=True)

    print(f"[OK] Создано {len(df)} отзывов")
    print(f"\nРаспределение:")
    print(df['sentiment'].value_counts())

    # Сохраняем
    print("\n[3/3] Сохранение...")
