# Расширяем датасет вариациями
def expand_reviews(reviews, sentiment, target_count=500):
    """Расширяет список отзывов до нужного количества"""
    expanded = []
    variations = [
        lambda x: x,
        lambda x: x + " Спасибо за внимание.",
        lambda x: x + " Моё мнение.",
        lambda x: "Честно говоря, " + x.lower(),
        lambda x: "По моему мнению, " + x.lower(),
        lambda x: x + " Вот такие дела.",
        lambda x: x + " Это моя оценка.",
        lambda x: "Скажу честно: " + x.lower(),
    ]

    while len(expanded) < target_count:
        for review in reviews:
            for variation in variations:
                expanded.append(variation(review))
                if len(expanded) >= target_count:
                    break
            if len(expanded) >= target_count:
                break

    return expanded[:target_count]

